#!/usr/bin/env node
// Generates the Essential Eight -> ISM mapping table in docs/essential-eight.md
// directly from data/essential-eight/controls.json, so the documentation can never
// drift from the catalogue (the two used to be hand-maintained separately and had
// contradicted each other).
//
//   node scripts/generate-ism-mapping.mjs           # rewrite the table in place
//   node scripts/generate-ism-mapping.mjs --check    # fail (exit 1) if out of sync
//
// The generated block lives between the AUTOGEN markers in docs/essential-eight.md.
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const catalogPath = path.join(repoRoot, 'data/essential-eight/controls.json');
const docPath = path.join(repoRoot, 'docs/essential-eight.md');

const BEGIN = '<!-- BEGIN AUTOGEN: ism-mapping (generated by scripts/generate-ism-mapping.mjs — do not edit by hand) -->';
const END = '<!-- END AUTOGEN: ism-mapping -->';

function buildBlock(catalog) {
  const rows = [];
  for (const strategy of catalog) {
    for (const req of strategy.requirements) {
      const ism = Array.isArray(req.ismMapping) && req.ismMapping.length
        ? req.ismMapping.join(', ')
        : '—';
      rows.push(`| ${strategy.strategy} | ${req.id} | ${req.level} | ${ism} |`);
    }
  }
  return [
    BEGIN,
    '## Essential Eight → ISM Mapping Index',
    '',
    '> **Provisional starter mappings — not authoritative.** The `ISM-xxxx` references below are',
    '> unverified candidates and have **not** been reconciled against the authoritative *Essential Eight',
    '> Maturity Model and ISM Mapping (December 2023)*. Do not rely on them for a real assessment. See',
    '> [`data/essential-eight/PROVENANCE.md`](../data/essential-eight/PROVENANCE.md) for source and status.',
    '>',
    '> This table is generated from `data/essential-eight/controls.json` by',
    '> `scripts/generate-ism-mapping.mjs`. Edit the catalogue, not this file.',
    '',
    '| Strategy | Req ID | Level | Provisional ISM mapping |',
    '| :--- | :--- | :--- | :--- |',
    ...rows,
    END,
  ].join('\n');
}

const catalog = JSON.parse(fs.readFileSync(catalogPath, 'utf8'));
const block = buildBlock(catalog);
const doc = fs.readFileSync(docPath, 'utf8');

const beginIdx = doc.indexOf(BEGIN);
const endIdx = doc.indexOf(END);
const isCheck = process.argv.includes('--check');

if (beginIdx === -1 || endIdx === -1 || endIdx < beginIdx) {
  console.error(`ERROR: AUTOGEN markers not found in ${path.relative(repoRoot, docPath)}.`);
  process.exit(1);
}

const updated = doc.slice(0, beginIdx) + block + doc.slice(endIdx + END.length);

if (isCheck) {
  if (updated !== doc) {
    console.error('ERROR: docs/essential-eight.md ISM mapping table is out of sync with controls.json.');
    console.error('Run: npm run docs:mappings');
    process.exit(1);
  }
  console.log('ISM mapping table is in sync with controls.json.');
} else {
  if (updated !== doc) {
    fs.writeFileSync(docPath, updated);
    console.log('Regenerated ISM mapping table in docs/essential-eight.md.');
  } else {
    console.log('ISM mapping table already up to date.');
  }
}
